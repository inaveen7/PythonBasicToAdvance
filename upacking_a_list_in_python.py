# program to understand unpack a collection in python

'''
If you have a collection of variables in a List, Tuple, etc..
Python allows you to extract the values into variables.
This is called UnPacking.
'''

# Unpacking a List

friuts =['Mango','Apple','Banana']

y,x,z = friuts

# Displaying collection after unpacking into variables

print(x)
print(y)
print(z)

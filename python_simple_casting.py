# program to understand the casting in python

'''
If you want to specify the data type of a variable, this can be done with casting

below is the example
'''

x=str(3)        # x will be '3'
y=int(3)        # y will be  3
z=float(3)      # z will be  3.0



#dispalying x,y and z values
print(x)
print(y)
print(z)

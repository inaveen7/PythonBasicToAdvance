#Program to understand variable creation in python

'''
In python variables are created when you assign a value to it
'''

x=5        #the variable x is created, as we have assigned a value to it.
y="Naveen" #the variable y is created, as we have assigned a value to it.


print(x) #prints value which is stored in the variable 'x'

print(y) #prints the value which is stored in the variable 'y'.



print(type(x)) #prints the data type of variable 'x'.

print(type(y)) # prints the data type of variable 'y'.
